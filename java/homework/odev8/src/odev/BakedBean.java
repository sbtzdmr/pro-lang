package odev;

public class BakedBean {
	
	private String level = "baked";
	private String goesWith = "pizza";
	
	public String getLevel() {
	return(level);
	}
	
	public void setLevel(String newLevel) {
	level = newLevel;
	}
	
	public String getGoesWith() {
	return(goesWith);
	}
	
	public void setGoesWith(String dish) {
	goesWith = dish;
	}
}


